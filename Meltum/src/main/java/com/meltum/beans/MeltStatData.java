package com.meltum.beans;

public class MeltStatData {
	 
	private String elemId;
	private String id;
	private Double earnAmount;
	private String name;
	private String type;
    private Double usedAmount;
	/**
	 * @return the elemId
	 */
	public String getElemId() {
		return elemId;
	}
	/**
	 * @param elemId the elemId to set
	 */
	public void setElemId(String elemId) {
		this.elemId = elemId;
	}
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the earnAmount
	 */
	public Double getEarnAmount() {
		return earnAmount;
	}
	/**
	 * @param earnAmount the earnAmount to set
	 */
	public void setEarnAmount(Double earnAmount) {
		this.earnAmount = earnAmount;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the usedAmount
	 */
	public Double getUsedAmount() {
		return usedAmount;
	}
	/**
	 * @param usedAmount the usedAmount to set
	 */
	public void setUsedAmount(Double usedAmount) {
		this.usedAmount = usedAmount;
	}
}
