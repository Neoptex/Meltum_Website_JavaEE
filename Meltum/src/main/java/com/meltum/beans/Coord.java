package com.meltum.beans;

public class Coord {
	private Double y;
	private Double x;
	
	/**
	 * @return the latitude
	 */
	public Double getY() {
		return y;
	}
	/**
	 * @param y the latitude to set
	 */
	public void setY(Double y) {
		this.y = y;
	}
	/**
	 * @return the longitude
	 */
	public Double getX() {
		return x;
	}
	/**
	 * @param x the longitude to set
	 */
	public void setX(Double x) {
		this.x = x;
	}
	

	
	
}
